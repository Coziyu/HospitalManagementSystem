package org.hms.entities;

/**
 * Enum representing various roles a user can have within a system.
 * The roles include:
 * <p>
 * - PATIENT: Represents a user who is a patient.
 * - DOCTOR: Represents a user who is a doctor.
 * - PHARMACIST: Represents a user who is a pharmacist.
 * - ADMINISTRATOR: Represents a user who has administrative privileges.
 */
public enum UserRole {
    PATIENT,
    DOCTOR,
    PHARMACIST,
    ADMINISTRATOR
}
